package day03.arrlist;

public class Stu2 {
    String name;
    String age;

    public Stu2() {
    }

    public Stu2(String name, String age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
